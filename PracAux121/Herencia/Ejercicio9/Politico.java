public class Politico {
    public String profecion;
    public int añosExp;

    public Politico(String profecion, int añosExp){
        this.profecion=profecion;
        this.añosExp=añosExp;
    }

    public String getProfecion() {
        return profecion;
    }

    public int getAñosExp() {
        return añosExp;
    }
}
